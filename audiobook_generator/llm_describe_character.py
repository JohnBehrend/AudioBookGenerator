def load_characters(characters_file: str) -> list:
    """Load characters from a JSON file."""
    with open(characters_file, 'r', encoding='utf-8') as f:
        data = json.load(f)
    return data.get('characters', [])


# compare_characters is now imported from utils (uses simple substring matching)


def find_duplicate_characters(characters: list) -> dict:
    """Find groups of duplicate characters based on name similarity.

    Returns a dict mapping canonical character name to list of duplicate names.
    """
    canonical_to_duplicates: dict = {}

    for char in characters:
        if char == "narrator":
            continue

        found_canonical = None
        for canonical in canonical_to_duplicates.keys():
            if compare_characters(char, canonical):
                found_canonical = canonical
                break

        if found_canonical:
            canonical_to_duplicates[canonical].append(char)
        else:
            canonical_to_duplicates[char] = []

    # Filter out entries with no duplicates
    return {k: v for k, v in canonical_to_duplicates.items() if v}


def create_duplicate_replacement_map(duplicates: dict) -> dict:
    """Create a mapping from each duplicate name to its canonical name.

    Args:
        duplicates: Dict mapping canonical name to list of duplicate names

    Returns:
        Dict mapping duplicate character names to their canonical name
    """
    replacement_map = {}
    for canonical, duplicates_list in duplicates.items():
        for dup_name in duplicates_list:
            replacement_map[dup_name] = canonical
    return replacement_map


def deduplicate_descriptions(descriptions: dict, duplicates: dict, verbose: bool = False) -> dict:
    """Deduplicate character descriptions based on duplicate character mappings.

    For each group of duplicate characters:
    - Keep the canonical character's description
    - Add "See <canonical> (same character)." for duplicates

    Args:
        descriptions: Dict mapping character names to their descriptions
        duplicates: Dict mapping canonical name to list of duplicate names
        verbose: Enable verbose output

    Returns:
        Dict with duplicates replaced by references to canonical characters
    """
    deduped = descriptions.copy()

    for canonical, duplicates_list in duplicates.items():
        # Add references for duplicates
        for dup_name in duplicates_list:
            if dup_name in descriptions:
                if verbose:
                    print(f"Marking '{dup_name}' as duplicate of '{canonical}'")
                deduped[dup_name] = f"See {canonical} (same character)."
            else:
                if verbose:
                    print(f"Duplicate '{dup_name}' not found in descriptions")

    return deduped


def load_chapter_text(chapter_file: str) -> str:
    """Load text from a chapter file."""
    with open(chapter_file, 'r', encoding='utf-8') as f:
        return f.read()


def find_chapters_with_character(chapter_texts: list, chapter_files: list, character_name: str) -> list:
    """Find chapters where a character is mentioned.

    Args:
        chapter_texts: List of chapter text content
        chapter_files: List of chapter file paths (in matching order)
        character_name: The character name to search for

    Returns:
        List of (chapter_file, chapter_text) tuples where character appears
    """
    chapters_with_char = []
    char_lower = character_name.lower()

    for text, filepath in zip(chapter_texts, chapter_files):
        if char_lower in text.lower():
            chapters_with_char.append((filepath, text))

    return chapters_with_char


def lookup_character_on_wiki(character_name: str, wiki_url_template: str = "") -> str:
    """Look up a character on a wiki for additional context.

    Args:
        character_name: The name of the character to look up
        wiki_url_template: URL template with {name} placeholder for the character name

    Returns:
        A string with biographical information from the wiki, or empty string if not found
    """
    # Format the URL with the character name
    url = wiki_url_template.format(name=character_name.replace(" ", "_"))

    try:
        headers = {"User-Agent": "Mozilla/5.0 (Audiobook Character Describer)"}
        response = requests.get(url, headers=headers, timeout=10)
        response.raise_for_status()

        soup = BeautifulSoup(response.content, "html.parser")

        # Check if it's a disambiguation page or doesn't exist
        content = soup.find("div", class_="mw-parser-output")
        if not content:
            return ""

        # Get the first paragraph (usually the most relevant)
        first_para = content.find("p")
        if first_para:
            text = first_para.get_text(strip=True)
            # Clean up citation brackets like [1], [2], etc.
            text = re.sub(r'\[\d+\]', '', text)
            return text[:1000]  # Limit to first 1000 chars

    except requests.RequestException:
        pass

    return ""


def build_character_context(characters: list, chapter_texts: list, chapter_files: list = [], wiki_url_template: str = "") -> str:
    """Build context string with character names and their appearances.

    Args:
        characters: List of character names
        chapter_texts: List of chapter text content
        chapter_files: List of chapter file paths (used for finding relevant chapters)
        wiki_url_template: Optional URL template for wiki lookup (with {name} placeholder)

    Returns:
        Context string for the LLM prompt
    """
    context = "Characters to describe:\n"
    for char in characters:
        context += f"- {char}\n"

    # Add wiki lookup context if enabled
    if wiki_url_template:
        context += "\n\nAdditional context from wiki:\n"
        for char in characters:
            wiki_info = lookup_character_on_wiki(char, wiki_url_template)
            if wiki_info:
                context += f"--- {char} ---\n{wiki_info}\n\n"

    # Build character-specific context using relevant chapters
    if chapter_files and len(chapter_files) == len(chapter_texts):
        context += "\n\nRelevant dialogue and context from the book:\n"
        for char in characters:
            relevant_chapters = find_chapters_with_character(chapter_texts, chapter_files, char)
            if relevant_chapters:
                context += f"\n--- {char} Context ---\n"
                # Use up to 2 most relevant chapters, first 800 chars each
                for filepath, text in relevant_chapters[:2]:
                    context += f"[From {filepath.name}]\n"
                    context += text[:800] + "\n\n"
    else:
        # Fallback to first 3 chapters for all characters
        context += "\n\nRelevant dialogue and context from the book:\n"
        for i, text in enumerate(chapter_texts[:3]):
            context += f"--- Chapter {i+1} Excerpt ---\n"
            context += text[:1000] + "\n\n"
    return context


def describe_character(client: OpenAI, model: str, character: str, context: str) -> str:
    """Ask the LLM to describe a single character."""
    messages = [
        {"role": "system", "content": CHARACTER_DESCRIPTION_PROMPT},
        {"role": "user", "content": f"Describe this character in detail:\n\nCharacter name: {character}\n\nContext:\n{context}"}
    ]

    try:
        response = client.chat.completions.create(
            model=model,
            messages=messages,
            temperature=0.7,
        )
        return response.choices[0].message.content
    except Exception as e:
        return f"Error describing character: {e}"


def describe_all_characters(client: OpenAI, model: str, characters: list, context: str) -> dict:
    """Ask the LLM to describe all characters at once."""
    messages = [
        {"role": "system", "content": CHARACTER_DESCRIPTION_PROMPT},
        {"role": "user", "content": f"Describe all these characters in detail. Return a JSON object with character names as keys and descriptions as values.\n\nCharacters:\n{chr(10).join(characters)}\n\nContext:\n{context}"}
    ]

    try:
        response = client.chat.completions.create(
            model=model,
            messages=messages,
            temperature=0.7,
        )
        content = response.choices[0].message.content
        # Try to parse JSON from response
        try:
            # Remove markdown code blocks if present
            if content.startswith('```json'):
                content = content[7:]
            if content.startswith('```'):
                content = content[3:]
            if content.endswith('```'):
                content = content[:-3]
            content = content.strip()
            return json.loads(content)
        except json.JSONDecodeError:
            # If parsing fails, return raw content
            return {char: content for char in characters}
    except Exception as e:
        print(f"Error describing characters: {e}", file=sys.stderr)
        return {}


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Describe characters from an audiobook using an LLM"
    )
    parser.add_argument(
        "characters_file",
        nargs="?",
        default="characters.json",
        help="Path to characters.json file (generated by analyze_chapters.py)"
    )
    parser.add_argument(
        "chapters_dir",
        nargs="?",
        default="chapters",
        help="Directory containing chapter text files"
    )
    parser.add_argument(
        "--verbose", "-v",
        action="store_true",
        help="Enable verbose printing for debug."
    )
    parser.add_argument(
        "--api_key",
        "-k",
        metavar="API_KEY",
        default=LLM_SETTINGS["api_key"],
        help="Provide custom API key."
    )
    parser.add_argument(
        "--port",
        "-p",
        metavar="PORT",
        default=LLM_SETTINGS["port"],
        help="Provide custom port for inference."
    )
    parser.add_argument(
        "--model",
        "-m",
        default=LLM_SETTINGS["default_model"],
        help="Model name to use for inference."
    )
    parser.add_argument(
        "--single-character",
        metavar="CHARACTER",
        help="Describe a single specific character."
    )
    parser.add_argument(
        "--wiki-url",
        metavar="URL",
        dest="wiki_url_template",
        help="Enable lookup on wiki using URL template (use {name} placeholder for character name)."
    )
    parser.add_argument(
        "--output-dir",
        metavar="DIR",
        dest="output_dir",
        default=str(OUTPUT_DIR),
        help="Directory to save output files (characters_descriptions.json and duplicate_replacement_map.json)."
    )
    args = parser.parse_args()

    client = get_llm_client(args.api_key, args.port)

    # Load characters from characters.json or extract from map files
    if os.path.exists(args.characters_file):
        characters = load_characters(args.characters_file)
        print(f"Loaded {len(characters)} characters from {args.characters_file}")
    else:
        # Try to extract characters from map files in the output_dir (chapters directory)
        chapters_path = Path(args.output_dir)
        if chapters_path.is_dir():
            characters = get_characters_from_map_files(chapters_path)
            print(f"Extracted {len(characters)} characters from map files in {args.output_dir}")
        else:
            print(f"Characters file not found: {args.characters_file}", file=sys.stderr)
            sys.exit(1)

    # Load chapter texts for context
    chapters_dir = Path(args.chapters_dir)
    chapter_texts = []
    chapter_files = []
    if chapters_dir.is_dir():
        chapter_files = sorted(chapters_dir.glob("chapter_*.txt"))
        for chapter_file in chapter_files:
            chapter_texts.append(load_chapter_text(str(chapter_file)))
        if args.verbose:
            print(f"Loaded {len(chapter_texts)} chapter files for context")

    # Describe characters
    if args.single_character:
        if args.single_character not in characters:
            print(f"Character '{args.single_character}' not found in characters list.", file=sys.stderr)
            sys.exit(1)
        if args.verbose:
            print(f"Describing single character: {args.single_character}")
        context = build_character_context(characters, chapter_texts, chapter_files, args.wiki_url_template)
        description = describe_character(client, args.model, args.single_character, context)
        print(f"\nDescription for '{args.single_character}':")
        print(description)
    else:
        # Use the public function for batch description
        descriptions = describe_characters(
            output_dir=args.output_dir,
            characters_file=args.characters_file,
            chapters_dir=args.chapters_dir,
            api_key=args.api_key,
            port=args.port,
            model=args.model,
            single_character=None,
            wiki_url_template=args.wiki_url_template,
            verbose=args.verbose
        )

        # Print summary
        print("\nCharacter descriptions generated:")
        for char, desc in descriptions.items():
            # Show first line of description
            first_line = desc.split('\n')[0][:80]
            # Handle Windows console encoding issues
            try:
                print(f"  - {char}: {first_line}...")
            except UnicodeEncodeError:
                # Fallback for Windows console
                safe_line = first_line.encode('utf-8', errors='replace').decode('utf-8')
                print(f"  - {char}: {safe_line}...")
